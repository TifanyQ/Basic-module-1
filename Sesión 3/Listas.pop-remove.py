"Listas en Python"

"Listas (pop() - Quita elementos de una posición dada)"

paises = ["Perú","España","Brasil","Colombia"]

print("Mi lista actualizada es la siguiente: {}".format(paises))

paises.pop(2)

print("Mi lista actualizada es la siguiente: {}".format(paises))

paises.pop(0)

print("Mi lista actualizada es la siguiente: {}".format(paises))

"Listas (insert())- Insertar elementos en una posición dada"

lista=[27,65,41,27,92]

lista.insert(1,200)

print("El valot de mi lista a ser actualizada es {}".format(lista))

lista.insert(len(lista),500)

print("El valot de mi lista a ser actualizada es {}".format(lista))

"Listas (remove)"

lista_1=["Java","Rubi","PHP","Ja"]

lista_1.remove("Java")

print("El valot de mi lista a ser actualizada es {}".format(lista_1))
